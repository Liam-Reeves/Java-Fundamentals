public class Variables {
// Container for storing data values
// Variables in Java: integers, 
// floating-point numbers, characters, strings, booleans
 public static void main(String[] args){
    int x = 5;// integer variable
    System.out.println(x);

    double y = 5.99; // floating-point variable
    System.out.println(y);

    char z = 'A';
    System.out.println(z);

    String name = "John";
    System.out.println(name); 

    boolean isCodingFun = true;
    System.out.println(isCodingFun);

 }
}
