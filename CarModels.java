public class CarModels {
    public static void main(String[] args) {
        // Create an array of several car models
        String[] carModels = {
            "Toyota Camry", 
            "Honda Civic",
            "Ford Mustang",
            "Tesla Model 3",
            "BMW X5",
            "Chevrolet Corvette"
        };

        // Display the car models
        System.out.println("List of Car Models:");
        for (String model : carModels) {
            System.out.println(model);
        }

        
    }
}